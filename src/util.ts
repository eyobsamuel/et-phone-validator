export function checkEthioTelecomFixedLine(phone: any, patterns: any) {
  let found = false
  patterns.forEach((pattern: any) => {
    if (pattern.test(phone))
      found = true
    else {
      // do-nothing
    }
  });
  if (found)
    return true
  else
    return false
}