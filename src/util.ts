export function addCountryCode(phone: string | number) {
  return phone.toString().replace(/^(?:\+251|251|0)?([1|2|3|4|5|9|7]\d{8})$/, "+251$1")
}

export function extractNumber(phone: string) {
  return phone.split(" ")
    .join("")
    .split("-")
    .join("")
    .split("(")
    .join("")
    .split(")")
    .join("");
}

